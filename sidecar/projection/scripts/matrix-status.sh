while IFS='|' read -r axis op pat slice; do
  [ -z "$axis" ] && continue
  st=$(witness_status "$pat")
  counted=$((counted+1))
  [ "$st" = "VERIFIED" ] && green=$((green+1))
  rows+="| **$axis** | $op | \`$pat\` | $slice | $(icon "$st") |"$'\n'
done <<< "$cells"

